for ver in "${schemaVers[@]}"; do
  pkg="fix${ver}"
  echo "    case \"${ver}\":" >> "$out"
  echo "        return ${pkg}.FIX${ver}XML" >> "$out"
done
cat >> "$out" <<'EOF'
    default:
        // fallback to 44
        return fix44.FIX44XML
    }
}
EOF

echo "Done. Generated packages: ${schemaVers[*]} and chooseFixVersion.go."
